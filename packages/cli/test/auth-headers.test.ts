import { execFile, spawn } from "node:child_process"
import { createServer, type Server } from "node:http"
import { mkdtempSync, mkdirSync, rmSync, writeFileSync, existsSync, readFileSync } from "node:fs"
import { tmpdir } from "node:os"
import { join } from "node:path"
import { fileURLToPath } from "node:url"
import { afterEach, beforeEach, describe, expect, it } from "vitest"
import { init } from "../src/init"

const GUARD = fileURLToPath(new URL("../runtime/relayroom-ask-guard.mjs", import.meta.url))

/**
 * The runtime endpoints now want a bearer. These three callers are on the grace
 * path (the server still accepts them unauthenticated and logs a warning), so both
 * directions matter: the token must be SENT when the worktree has one, so the grace
 * can eventually be withdrawn, and NO header may be sent when it does not - an empty
 * `Bearer ` is a present-but-invalid credential that the server rejects outright.
 */

/** Records the Authorization header of every request, by path. */
function recordingHub(handler?: (path: string) => { status: number; body: string; headers?: Record<string, string> }) {
  const seen: { path: string; auth: string | undefined }[] = []
  const server: Server = createServer((req, res) => {
    const path = (req.url ?? "").split("?")[0] ?? ""
    seen.push({ path, auth: req.headers.authorization })
    const out = handler?.(path) ?? { status: 200, body: "{}" }
    res.writeHead(out.status, { "content-type": "application/json", ...(out.headers ?? {}) })
    res.end(out.body)
  })
  const port = new Promise<number>((resolve) => {
    server.listen(0, "127.0.0.1", () => resolve((server.address() as { port: number }).port))
  })
  return { server, port, seen, authFor: (p: string) => seen.find((s) => s.path === p)?.auth }
}

const MD_HANDLER = () => ({
  status: 200,
  body: "# RELAYROOM.md\n",
  headers: { "x-relayroom-project-slug": "demo" },
})

describe("relayroom init: RELAYROOM.md fetch", () => {
  let dir: string
  let hub: ReturnType<typeof recordingHub>
  let url: string
  let savedTmux: string | undefined

  beforeEach(async () => {
    dir = mkdtempSync(join(tmpdir(), "relayroom-init-"))
    hub = recordingHub(MD_HANDLER)
    url = `http://127.0.0.1:${await hub.port}`
    // init renames the CURRENT tmux session when it runs inside one. The test runner
    // may well be inside tmux, and renaming the developer's own session would be a
    // nasty surprise, so hide it for the duration.
    savedTmux = process.env.TMUX
    delete process.env.TMUX
  })

  afterEach(() => {
    hub.server.close()
    if (savedTmux !== undefined) process.env.TMUX = savedTmux
    rmSync(dir, { recursive: true, force: true })
  })

  it("sends the bearer passed on the command line", async () => {
    await init({ dir, code: "c1", part: "core", server: url, token: "tok-flag", tmuxCheck: false })
    expect(hub.authFor("/mcp/c1/relayroom-md")).toBe("Bearer tok-flag")
  })

  it("sends the token saved by a previous init (the re-init path passes no flags)", async () => {
    await init({ dir, code: "c1", part: "core", server: url, token: "tok-saved", tmuxCheck: false })
    await init({ dir, tmuxCheck: false }) // `relayroom init` with nothing else
    expect(hub.seen.filter((s) => s.path === "/mcp/c1/relayroom-md")).toHaveLength(2)
    expect(hub.seen[1]?.auth).toBe("Bearer tok-saved")
  })

  it("sends no authorization header on a first init, which has no token yet", async () => {
    await init({ dir, code: "c1", part: "core", server: url, tmuxCheck: false })
    expect(hub.authFor("/mcp/c1/relayroom-md")).toBeUndefined()
    expect(existsSync(join(dir, "RELAYROOM.md"))).toBe(true)
  })
})

/** Run a command, resolving with its exit code and output (never rejects). */
function run(cmd: string, args: string[], opts: { cwd?: string } = {}): Promise<{ code: number; stdout: string; stderr: string }> {
  return new Promise((resolve) => {
    execFile(cmd, args, { cwd: opts.cwd, timeout: 20_000 }, (err, stdout, stderr) => {
      const code = err && typeof (err as { code?: unknown }).code === "number" ? (err as { code: number }).code : err ? 1 : 0
      resolve({ code, stdout: String(stdout), stderr: String(stderr) })
    })
  })
}

describe("generated rr.sh: update", () => {
  let dir: string
  let hub: ReturnType<typeof recordingHub>
  let url: string
  let savedTmux: string | undefined

  // rr.sh is generated by init, so this exercises the script that actually ships.
  const initWorktree = async (token?: string) => {
    await init({ dir, code: "c1", part: "core", server: url, token, tmuxCheck: false })
  }

  beforeEach(async () => {
    dir = mkdtempSync(join(tmpdir(), "relayroom-rrsh-"))
    hub = recordingHub(MD_HANDLER)
    url = `http://127.0.0.1:${await hub.port}`
    savedTmux = process.env.TMUX
    delete process.env.TMUX
  })

  afterEach(() => {
    hub.server.close()
    if (savedTmux !== undefined) process.env.TMUX = savedTmux
    rmSync(dir, { recursive: true, force: true })
  })

  it("passes the token to curl when the worktree has one", async () => {
    await initWorktree("tok-rrsh")
    const { code } = await run("bash", [join(dir, "rr.sh"), "update"], { cwd: dir })
    expect(code).toBe(0)
    expect(hub.seen.filter((s) => s.path === "/mcp/c1/relayroom-md").at(-1)?.auth).toBe("Bearer tok-rrsh")
  })

  it("omits the header entirely when there is no token, and still updates", async () => {
    await initWorktree()
    const before = readFileSync(join(dir, "RELAYROOM.md"), "utf8")
    const { code, stderr } = await run("bash", [join(dir, "rr.sh"), "update"], { cwd: dir })
    expect(code, stderr).toBe(0)
    expect(hub.seen.filter((s) => s.path === "/mcp/c1/relayroom-md").at(-1)?.auth).toBeUndefined()
    expect(readFileSync(join(dir, "RELAYROOM.md"), "utf8")).toBe(before)
  })
})

